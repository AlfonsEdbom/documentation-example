def multiply(a: float, b: float) -> float:
    """
    Multiply two numbers.

    :param a: First number.
    :param b: Second number.
    :return: The product of a and b.
    """
    return a * b


def add(a: float, b:float) -> float:
    """
    Add two numbers.

    :param a: First number.
    :param b: Second number
    :return: The sum of a and b
    """
    return a + b